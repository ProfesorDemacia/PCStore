package CapaDTO;

/**
 *
 * @author Cesar
 */
public class Usuario {
    
    private String rut_usu;
    private String nombre_usu;
    private String apellido_usu;
    private int sueldo_usu;
    private String correo_usu;
    private String contrasena_usu;
    private int id_tipo_usu;

    public String getRut_usu() {
        return rut_usu;
    }

    public void setRut_usu(String rut_usu) {
        this.rut_usu = rut_usu;
    }

    public String getNombre_usu() {
        return nombre_usu;
    }

    public void setNombre_usu(String nombre_usu) {
        this.nombre_usu = nombre_usu;
    }

    public String getApellido_usu() {
        return apellido_usu;
    }

    public void setApellido_usu(String apellido_usu) {
        this.apellido_usu = apellido_usu;
    }

    public int getSueldo_usu() {
        return sueldo_usu;
    }

    public void setSueldo_usu(int sueldo_usu) {
        this.sueldo_usu = sueldo_usu;
    }

    public String getCorreo_usu() {
        return correo_usu;
    }

    public void setCorreo_usu(String correo_usu) {
        this.correo_usu = correo_usu;
    }

    public int getId_tipo_usu() {
        return id_tipo_usu;
    }

    public void setId_tipo_usu(int id_tipo_usu) {
        this.id_tipo_usu = id_tipo_usu;
    }

    public String getContrasena_usu() {
        return contrasena_usu;
    }

    public void setContrasena_usu(String contrasena_usu) {
        this.contrasena_usu = contrasena_usu;
    }
    
    
    
    
}
